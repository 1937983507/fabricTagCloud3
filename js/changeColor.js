// 初始化 备选背景色
function initBackground() {
    // 设置备选背景色
    const backgroundColorItem = document.getElementById('background-color-item');
    // 遍历backgroundColor数组，并创建对应的HTML元素
    backgroundColor.forEach(color => {
        // 创建item-color元素
        const itemColor = document.createElement('div');
        itemColor.classList.add('item-color');
        // 创建child-color元素
        const childColor = document.createElement('div');
        childColor.classList.add('child-color');
        childColor.style.background = color;
        // 将child-color添加到item-color中
        itemColor.appendChild(childColor);
        // 将item-color添加到background-color-item中
        backgroundColorItem.appendChild(itemColor);
    });
}

// 初始化 备选色带
function initRibbonColor() {
    const ribbonElement = document.getElementById('ribbon');
    ribbonElement.innerHTML = "";
    // 遍历ribbonColor数组，并创建对应的HTML元素
    ribbonColor[nowColorDispersedNum - 3].forEach(colors => {
        // 创建item-color元素
        const itemColor = document.createElement('div');
        itemColor.classList.add('item-color');
        // 遍历colors数组，为每个颜色创建child-color元素
        colors.forEach(color => {
            const childColor = document.createElement('div');
            childColor.classList.add('child-color');
            childColor.style.background = color;
            // 将child-color添加到item-color中
            itemColor.appendChild(childColor);
        });
        // 将item-color添加到ribbonElement中
        ribbonElement.appendChild(itemColor);
    });
}

// 初始化 图例
function initLegend() {
    // 获取当前文字配色的容器和图例容器
    const currentColorBar = document.getElementById('peise-current-color-bar');
    const legendColor = document.getElementById('legendColor');
    // 清空图例容器中的内容
    legendColor.innerHTML = '';
    // 遍历当前文字配色容器中的每个颜色项
    currentColorBar.childNodes.forEach(child => {
        // 确保节点是元素节点
        if (child.nodeType === 1) {
            // 创建一个新的div元素作为图例中的颜色方块
            const colorDiv = document.createElement('div');
            colorDiv.style.backgroundColor = child.style.backgroundColor; // 设置颜色
            // 将颜色方块添加到图例容器中
            legendColor.appendChild(colorDiv);
        }
    });
}



// 颜色改变交互（总）
