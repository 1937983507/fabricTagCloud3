// 改变canvas标签云的色带
function changeCanvasRibbon() {

    // 初始化标签颜色
    initTagColor(selectedPOI);
    // 初始化影像金字塔
    initpoisPyramid(selectedPOI);

    // 逐一改变标签的色带
    var nowJson = poisPyramid[tagCloudScale];
    var nowJsonLength = Object.keys(nowJson).length;
    for (var i = 0; i < nowJsonLength; i++) {
        canvas.item(i + 1).set({ fill: nowJson[i].fontColor });
    }
    // 更新画布，重绘
    canvas.renderAll();
}


