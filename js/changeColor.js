// 颜色翻转
function colorFlip() {
    // 获取当前的配色方案
    var colorItems = document.getElementById("peise-current-color-bar").getElementsByClassName("item");
    var colors = [];
    // 遍历每个元素并提取背景颜色
    for (var i = 0; i < colorItems.length; i++) {
        var bgColor = window.getComputedStyle(colorItems[i]).getPropertyValue("background-color");
        colors.push(bgColor);
    }

    // 配色方案翻转
    colors.reverse();

    // 获取peise-current-color-bar元素
    const currentColorBar = document.getElementById('peise-current-color-bar');
    // 清空peise-current-color-bar的子元素
    currentColorBar.innerHTML = '';
    // 遍历childColors，将颜色应用到peise-current-color-bar中
    colors.forEach(color => {
        const newColorItem = document.createElement('div');
        newColorItem.classList.add('item');
        newColorItem.style.backgroundColor = color;
        currentColorBar.appendChild(newColorItem);
    });
    // 初始化图例
    initLegend();
    // 改变图例事件
    changeLegend();
    // 改变canvas色带
    changeCanvasRibbon();

}